    //Fonction pour l'enregistrement d'une Programme********************************************************************************************************************
    public static void enregistrerChapitre() {
        // Récuperation des donnéses du formulaire
        String aCode = codeChap.getText().trim();
        //int pidBudget = Integer.parseInt(idBud.getText());
        String aLibele = libeleChap.getText().trim();

        if (aCode.isBlank() || idAc.getText().isBlank() || aLibele.isBlank()) {
            JOptionPane.showMessageDialog(null, "Des cahmps sont vides");
        } else {
            int pBudget = Integer.parseInt(idAc.getText());
            Chapitre chapitre = new Chapitre(pBudget, aCode, aLibele); // Créez un objet InterfaceStructure     
            ChapitreController.saveChapitre(chapitre);  // Enregistrez  dans la base de données            
            actionBug.setSelectedIndex(0);
            idAc.setText("");
            codeChap.setText("");
            libeleChap.setText("");
        }

    }

