    //Modifier une structure********************************************************************************************************************
    public static void modifierChapitre() {
        // Récuperation des donnéses du formulaire
        String aCode = codeChap.getText().trim();
        String aLibele = libeleChap.getText().trim();
        if (aCode.isBlank() || idAc.getText().isBlank() || aLibele.isBlank()) {
            JOptionPane.showMessageDialog(null, "Des cahmps sont vides");
        } else {
            int pBudget = Integer.parseInt(idAc.getText());
            rep = JOptionPane.showConfirmDialog(null, "Voulez-vous vraiment modifier ce programme?", "Modification d'un programme", JOptionPane.YES_NO_OPTION);
            if (rep == JOptionPane.YES_OPTION) {
                Chapitre chapitre = new Chapitre(pBudget, aCode, aLibele);
                ChapitreController.updateChapitre(chapitre); // Executer la méthode de modification dans la base de données
                JOptionPane.showMessageDialog(null, "Modification validée");
                actionBug.setSelectedIndex(0);
                idAc.setText("");
                codeChap.setText("");
                libeleChap.setText("");
            }
        }

    }

    //Supprimer une structure********************************************************************************************************************
    public static void supprimerChapitre() {
        rep = JOptionPane.showConfirmDialog(null, "Voulez-vous vraiment supprimer ce programme?", "Suppression d'un programme", JOptionPane.YES_NO_OPTION);
        if (rep == JOptionPane.YES_OPTION) {
            Chapitre chapitre = new Chapitre();
            ChapitreController.deleteChapitre(chapitre);// Executer la méthode de suppression des données    
            JOptionPane.showMessageDialog(null, "Programme supprimé");
            actionBug.setSelectedIndex(0);
            idAc.setText("");
            codeChap.setText("");
            libeleChap.setText("");
        }
    }

