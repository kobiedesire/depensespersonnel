    //Fonction pour l'enregistrement d'une Programme********************************************************************************************************************
    public static void enregistrerArticle() {
        // Récuperation des donnéses du formulaire
        String aCode = codeArticle.getText().trim();
        //int pidBudget = Integer.parseInt(idBud.getText());
        String aLibele = libeleArticle.getText().trim();

        if (aCode.isBlank() || idActivi.getText().isBlank() || aLibele.isBlank()) {
            JOptionPane.showMessageDialog(null, "Des cahmps sont vides");
        } else {
            int pBudget = Integer.parseInt(idActivi.getText());
            Article article = new Article(pBudget, aCode, aLibele); // Créez un objet InterfaceStructure     
            ArticleController.saveArticle(article);  // Enregistrez  dans la base de données            
            activiteBug.setSelectedIndex(0);
            idActivi.setText("");
            codeArticle.setText("");
            libeleArticle.setText("");
        }

    }

