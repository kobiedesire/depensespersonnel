    //Modifier une structure********************************************************************************************************************
    public static void modifierArticle() {
        // Récuperation des donnéses du formulaire
        String aCode = codeArticle.getText().trim();
        String aLibele = libeleArticle.getText().trim();
        if (aCode.isBlank() || idActivi.getText().isBlank() || aLibele.isBlank()) {
            JOptionPane.showMessageDialog(null, "Des cahmps sont vides");
        } else {
            int pBudget = Integer.parseInt(idActivi.getText());
            rep = JOptionPane.showConfirmDialog(null, "Voulez-vous vraiment modifier cet article?", "Modification d'un article", JOptionPane.YES_NO_OPTION);
            if (rep == JOptionPane.YES_OPTION) {
                Article article = new Article(pBudget, aCode, aLibele);
                ArticleController.updateArticle(article); // Executer la méthode de modification dans la base de données
                JOptionPane.showMessageDialog(null, "Modification validée");
                activiteBug.setSelectedIndex(0);
                idActivi.setText("");
                codeArticle.setText("");
                libeleArticle.setText("");
            }
        }

    }

    //Supprimer une structure********************************************************************************************************************
    public static void supprimerArticle() {
        rep = JOptionPane.showConfirmDialog(null, "Voulez-vous vraiment supprimer cet article?", "Suppression d'un article", JOptionPane.YES_NO_OPTION);
        if (rep == JOptionPane.YES_OPTION) {
            Article article = new Article();
           ArticleController.deleteArticle(article);// Executer la méthode de suppression des données    
            JOptionPane.showMessageDialog(null, "Article supprimé");
            activiteBug.setSelectedIndex(0);
            idActivi.setText("");
            codeArticle.setText("");
            libeleArticle.setText("");
        }
    }

