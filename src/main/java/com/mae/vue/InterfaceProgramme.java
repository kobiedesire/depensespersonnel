    //Fonction pour l'enregistrement d'une Programme********************************************************************************************************************
    public static void enregistrerProgramme() {
        // Récuperation des donnéses du formulaire

        String pCode = codePr.getText().trim();
        //int pidBudget = Integer.parseInt(idBud.getText());
        String pLibele = libelePr.getText().trim();

        if (pCode.isBlank() || idBud.getText().isBlank() || pLibele.isBlank()) {
            JOptionPane.showMessageDialog(null, "Des cahmps sont vides");
        } else {
            int pBudget = Integer.parseInt(idBud.getText());
            Programme programme = new Programme(pBudget, pCode, pLibele); // Créez un objet InterfaceStructure     
            ProgrammeController.saveProgramme(programme);  // Enregistrez  dans la base de données            
            exerciceBu.setSelectedIndex(0);
            idBud.setText("");
            codePr.setText("");
            libelePr.setText("");
        }

    }

