    //Modifier une structure********************************************************************************************************************
    public static void modifierProgramme() {
        // Récuperation des donnéses du formulaire
        String pCode = codePr.getText().trim();
        String pLibele = libelePr.getText().trim();
        if (pCode.isBlank() || idBud.getText().isBlank() || pLibele.isBlank()) {
            JOptionPane.showMessageDialog(null, "Des cahmps sont vides");
        } else {
            int pBudget = Integer.parseInt(idBud.getText());
            rep = JOptionPane.showConfirmDialog(null, "Voulez-vous vraiment modifier ce programme?", "Modification d'un programme", JOptionPane.YES_NO_OPTION);
            if (rep == JOptionPane.YES_OPTION) {
                Programme programme = new Programme(pBudget, pCode, pLibele);
                ProgrammeController.updateProgramme(programme); // Executer la méthode de modification dans la base de données
                JOptionPane.showMessageDialog(null, "Modification validée");
                exerciceBu.setSelectedIndex(0);
                idBud.setText("");
                codePr.setText("");
                libelePr.setText("");
            }
        }

    }

    //Supprimer une structure********************************************************************************************************************
    public static void supprimerProgramme() {
        rep = JOptionPane.showConfirmDialog(null, "Voulez-vous vraiment supprimer ce programme?", "Suppression d'un programme", JOptionPane.YES_NO_OPTION);
        if (rep == JOptionPane.YES_OPTION) {
            Programme programme = new Programme();
            ProgrammeController.deleteProgramme(programme);// Executer la méthode de suppression des données    
            JOptionPane.showMessageDialog(null, "Programme supprimé");
            exerciceBu.setSelectedIndex(0);
            idBud.setText("");
            codePr.setText("");
            libelePr.setText("");
        }
    }

