    public static void afficherDateDuJour() {
        LocalDate dateAujourdhui = LocalDate.now();
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        String dateFormatee = dateAujourdhui.format(formatter);
        dateJour.setText(dateFormatee);
    }

    public static void afficherHeure() {
        ScheduledExecutorService scheduler = Executors.newScheduledThreadPool(1);

        Runnable afficherHeure = () -> {
            LocalTime heureActuelle = LocalTime.now();
            DateTimeFormatter formatter = DateTimeFormatter.ofPattern("HH:mm:ss");
            heureJour.setText(heureActuelle.format(formatter));
            // System.out.println("Heure actuelle : " + heureActuelle.format(formatter));
        };

        // Exécute la tâche toutes les secondes
        scheduler.scheduleAtFixedRate(afficherHeure, 0, 1, TimeUnit.SECONDS);
    }
    
